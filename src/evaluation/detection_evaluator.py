    """
    Evaluates detection models on test data.
    
    Responsibilities:
    - Run model evaluation
    - Calculate mAP metrics
    - Save evaluation results
    - Generate experiment summaries
    - Copy training artifacts (CSV, plots) to organized directories
    """
    
    def __init__(self):
        """Initialize evaluator."""
        pass
    
    def organize_training_artifacts(self, output_dir: str) -> bool:
        """
        Organize Ultralytics training outputs into structured directories.
        
        Copies training history CSV and visualization plots from Ultralytics'
        default output structure to organized training/ and evaluation/ directories.
        
        Args:
            output_dir: Root output directory (e.g., outputs/detection_yolov8_v3/run_TIMESTAMP/)
            
        Returns:
            True if successful, False otherwise
        """
        output_path = Path(output_dir)
        
        # Based on the experiment script logic:
        # - Trainer receives: output_dir / 'training' as output_dir parameter
        # - Trainer sets: project=output_dir/training, name='train'
        # - Ultralytics creates: output_dir/training/train/
        # 
        # So we need to look in: output_dir/training/train/
        ultralytics_train_dir = output_path / 'training' / 'train'
        
        if not ultralytics_train_dir.exists():
            print(f'⚠ Warning: Ultralytics training output not found at {ultralytics_train_dir}')
            print(f'  Listing output_dir contents:')
            if output_path.exists():
                for item in output_path.iterdir():
                    print(f'    - {item.name}')
                # Also check training/ subdirectory
                training_dir = output_path / 'training'
                if training_dir.exists():
                    print(f'  Contents of training/:')
                    for item in training_dir.iterdir():
                        print(f'    - training/{item.name}')
            # Fallback: Ultralytics may have written to runs/detect/<relative output path>
            try:
                cwd = Path.cwd()
                alt_path = cwd / 'runs' / 'detect' / output_path.relative_to(cwd)
                alt_train_dir = alt_path / 'training' / 'train'
                if alt_train_dir.exists():
                    print(f'  Found fallback training output at {alt_train_dir}')
                    ultralytics_train_dir = alt_train_dir
                else:
                    print(f'  Fallback training output not found at {alt_train_dir}')
                    return False
            except Exception:
                return False
        
        print(f'✓ Found Ultralytics training output at: {ultralytics_train_dir}')
        
        success = True
        
        # The CSV and plots are already in output_dir/training/train/
        # We need to copy them to output_dir/training/ (parent level) and output_dir/evaluation/
        
        # 1. Copy training history CSV to training/ (same level, easier access)
        training_output_dir = output_path / 'training'
        
        results_csv = ultralytics_train_dir / 'results.csv'
        if results_csv.exists():
            csv_output = training_output_dir / 'training_history.csv'
            shutil.copy(results_csv, csv_output)
            print(f'✓ Training history CSV saved to: {csv_output}')
        else:
            print(f'⚠ Warning: results.csv not found at {results_csv}')
            success = False
        
        # 2. Copy visualization plots to evaluation/
        eval_output_dir = output_path / 'evaluation'
        eval_output_dir.mkdir(parents=True, exist_ok=True)
        
        # Key plots generated by Ultralytics. Newer releases may prefix curve files with "Box".
        plot_candidates = {
            'results.png': ['results.png'],
            'confusion_matrix.png': ['confusion_matrix.png'],
            'F1_curve.png': ['F1_curve.png', 'BoxF1_curve.png'],
            'P_curve.png': ['P_curve.png', 'BoxP_curve.png'],
            'R_curve.png': ['R_curve.png', 'BoxR_curve.png'],
            'PR_curve.png': ['PR_curve.png', 'BoxPR_curve.png'],
        }

        for target_name, candidates in plot_candidates.items():
            found = False
            for candidate in candidates:
                src_plot = ultralytics_train_dir / candidate
                if src_plot.exists():
                    dst_plot = eval_output_dir / target_name
                    shutil.copy(src_plot, dst_plot)
                    if candidate != target_name:
                        print(f'✓ Copied {candidate} to evaluation as {target_name}')
                    else:
                        print(f'✓ Copied {target_name} to evaluation')
                    found = True
                    break
            if not found:
                print(f'⚠ Warning: {target_name} not found in {ultralytics_train_dir}')
        
        return success
    
    def evaluate_yolov8(self, model, test_dataset: str, output_dir: str) -> Dict:
        """
        Evaluate YOLOv8 model on test set.
        
        Args:
            model: YOLOv8Detector instance
            test_dataset: Test dataset config path
            output_dir: Directory to save results (typically output_dir/evaluation/)
            
        Returns:
            Evaluation metrics dictionary
        """
        print("=" * 80)
        print("YOLOv8 MODEL EVALUATION")
        print("=" * 80)
        
        output_path = Path(output_dir).resolve()
        output_path.mkdir(parents=True, exist_ok=True)
        
        # Run validation using Ultralytics with explicit output location
        print(f"Evaluating on dataset: {test_dataset}")
        print(f"Saving validation results to: {output_path}")
        
        # Use Ultralytics val() with explicit project and name
        results = model.model.val(
            data=test_dataset,
            project=str(output_path),
            name='val',  # Creates output_dir/val/ subdirectory
            exist_ok=True
        )
        
        # Extract metrics
        metrics = {
            'mAP50': float(results.box.map50),
            'mAP50_95': float(results.box.map),
            'precision': float(results.box.mp),
            'recall': float(results.box.mr),
        }
        
        # Print results
        print(f'\nEvaluation Metrics:')
        print(f'  mAP@0.5: {metrics["mAP50"]:.4f}')
        print(f'  mAP@0.5:0.95: {metrics["mAP50_95"]:.4f}')
        print(f'  Precision: {metrics["precision"]:.4f}')
        print(f'  Recall: {metrics["recall"]:.4f}')
        
        # Save results
        metrics_path = output_path / 'evaluation_metrics.json'
        with open(metrics_path, 'w') as f:
            json.dump(metrics, f, indent=2)
        
        print(f'\nEvaluation results saved to: {output_path}')
        
        return metrics
    
    def generate_experiment_summary(
        self, 
        output_dir: str, 
        experiment_name: str,
        model_config: Dict,
        training_config: Dict,
        metrics: Dict,
        customization_details: str = ""
    ):
        """
        Generate a comprehensive Markdown summary for the experiment.
        
        Args:
            output_dir: Directory to save the summary
            experiment_name: Name of the experiment (e.g., "V1: Baseline")
            model_config: Dictionary containing model configurations
            training_config: Dictionary containing training hyperparameters
            metrics: Dictionary containing evaluation results
            customization_details: String describing architectural changes
        """
        output_path = Path(output_dir)
        summary_path = output_path / 'experiment_summary.md'
        
        with open(summary_path, 'w') as f:
            f.write(f'# Experiment {experiment_name}\n\n')
            f.write(f'**Date:** {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}\n\n')
            
            f.write(f'## Configuration\n\n')
            # Model specific configs
            if 'backbone' in model_config:
                f.write(f'- Model: YOLOv8{model_config["backbone"]}\n')
            if 'input_size' in model_config:
                f.write(f'- Input size: {model_config["input_size"]}\n')
            if 'confidence_threshold' in model_config:
                f.write(f'- Confidence threshold: {model_config["confidence_threshold"]}\n')
            if 'nms_iou_threshold' in model_config:
                f.write(f'- NMS IoU threshold: {model_config["nms_iou_threshold"]}\n')
            if 'model_yaml' in model_config and model_config['model_yaml']:
                f.write(f'- Custom YAML: `{model_config["model_yaml"]}`\n')
            
            if customization_details:
                f.write(f'- Customization: {customization_details}\n')
            
            f.write(f'\n## Training Hyperparameters\n\n')
            for key, value in training_config.items():
                f.write(f'- **{key}**: {value}\n')
            
            f.write(f'\n## Results\n\n')
            f.write(f'- mAP@0.5: {metrics.get("mAP50", 0):.4f}\n')
            f.write(f'- mAP@0.5:0.95: {metrics.get("mAP50_95", 0):.4f}\n')
            f.write(f'- Precision: {metrics.get("precision", 0):.4f}\n')
            f.write(f'- Recall: {metrics.get("recall", 0):.4f}\n')
            
            f.write(f'\n## Files\n\n')
            f.write(f'- Training history: `training/training_history.csv`\n')
            f.write(f'- Best model: `training/train/weights/best.pt`\n')
            f.write(f'- Evaluation metrics: `evaluation/evaluation_metrics.json`\n')

        print(f'Experiment summary saved to: {summary_path}')
        return summary_path
    
